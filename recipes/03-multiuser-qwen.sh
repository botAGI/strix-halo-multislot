#!/bin/bash
# Qwen3.6-35B-A3B в мультислоте: 181 tok/s агрегат на 32 запросах (тюненный узел).
# КВАНТ ИМЕЕТ ЗНАЧЕНИЕ (медианы 3 прогонов, тюненный узел; качество квантов
# не оценивалось, для прод-выбора добавьте свою оценку качества):
#   Q4_0   79.5/запрос | 158.7 @c8 | 178.1 @c32 — лучший НА ВСЕХ уровнях
#   IQ4_NL 76.3 | 153.7 | 166.9 — середина
#   Q4_K_M 75.7 | 149.3 | 160.4
# (ранний вывод «одиночный за Q4_K_M» из одиночных прогонов повторами ОПРОВЕРГНУТ)
# ОСОБЕННОСТЬ МОДЕЛИ: воспроизводимая «долина» агрегата на 10-20 одновременных
# запросах (обрыв со 152 до ~100 при переходе 8→10) и потолок ~180. Эффект следует
# за моделью, не за билдом и не за флагами: Gemma 4 на том же стенде долины не имеет.
# Qwen3.6 устроен как гибрид линейного внимания (Gated DeltaNet) с attention-слоями;
# первопричину долины мы не профилировали, так что это наблюдение, не диагноз.
# Если нагрузка живёт в зоне 10-20 запросов: либо Gemma, либо два сервера по -np 8
# (для Q4_K_M это дало +41% на 16 клиентах; Q4_0 от второго сервера не выигрывает).
set -e
MODELS_DIR="${MODELS_DIR:-/var/lib/llm-models}"
IMAGE="ghcr.io/ggml-org/llama.cpp@sha256:25932f6dde7478203be75a04651d210ff1a5f0ac7877fb61f4fa622943bea6df"

docker run -d --name llm-qwen \
  --restart unless-stopped \
  --device /dev/dri \
  --group-add 44 --group-add 992 \
  -v "$MODELS_DIR":/models:ro \
  -p 8080:8080 \
  -e VK_DRIVER_FILES=/usr/share/vulkan/icd.d/radeon_icd.json \
  "$IMAGE" \
  --host 0.0.0.0 --port 8080 --metrics \
  -m /models/Qwen3.6-35B-A3B-Q4_0.gguf \
  `# sha256 af1e0e8a6e16ea85da7cb49f2eddd6eff3789f796b2698d9c8b4ab126e2f297b` \
  -ngl 999 --flash-attn on \
  --cache-type-k q8_0 --cache-type-v q8_0 \
  --ubatch-size 2048 --batch-size 2048 --no-mmap \
  --ctx-size 262144 --parallel 32
# q8_0 KV: вдвое меньше памяти под KV-кэш, штраф ~2% против f16 — на 32 слотах того стоит.
